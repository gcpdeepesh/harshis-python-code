grade = eval(input("Enter your number grade (0-100): "))
if grade >= 90:
    print ("Congo, you got an A*")	
elif grade >= 80:
    print ("What a luck, you got an A!")
elif grade >= 70:
    print ("You got a B!")
elif grade >= 60:
    print ("You got a C")
elif grade <= 30:
    print ("Better luck next time because you got a D")
	
